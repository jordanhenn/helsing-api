    association TEXT NOT NULL,
    manager_firstname TEXT,
    manager_email TEXT,
    assigned_to INTEGER
        REFERENCES helsing_employee(e_id) ON DELETE CASCADE,
    date_added TIMESTAMP DEFAULT now() NOT NULL,
    fy_end TEXT,
    client_number TEXT,
    total_price TEXT,
    s_contract BOOLEAN,
    retainer BOOLEAN,
    worksheets_yr1 BOOLEAN,
    worksheets_yr2 BOOLEAN,
    worksheets_yr3 BOOLEAN,
    yr1_billed BOOLEAN,
    yr1_billed_date TIMESTAMP,
    yr2_billed BOOLEAN,
    yr2_billed_date TIMESTAMP,
    yr3_billed BOOLEAN,
    yr3_billed_date TIMESTAMP,
    sustainment_letter BOOLEAN,
    additional_notes TEXT
);

